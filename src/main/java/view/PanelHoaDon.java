    public boolean checkSearch() {
        return txtSearchMa.getText().isBlank() ? true : false;
    }

    public void searchMa() {
        if (checkSearch()) {
            JOptionPane.showConfirmDialog(this, "Sai");
        } else {
            list.removeAll(list);
            list.add(hoaDonService.getOneByMa(txtSearchMa.getText()));
            showResult(list);
        }
    }

    public void searchNV() {
        if (checkSearch()) {
            JOptionPane.showConfirmDialog(this, "Sai");
        } else {
            list.removeAll(list);
            list = hoaDonService.getByNV(txtSearchMa.getText());
            showResult(list);
        }
    }


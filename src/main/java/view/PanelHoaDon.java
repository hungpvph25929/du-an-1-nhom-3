public void getCBB() {
        if (cbbTT.getSelectedIndex() == 0) {
            list.removeAll(list);
            list = hoaDonService.getByOne(0);
            showResult(list);
        } else if (cbbTT.getSelectedIndex() == 1) {
            list.removeAll(list);
            list = hoaDonService.getByOne(1);
            showResult(list);
        } else if (cbbTT.getSelectedIndex() == 2) {
            list.removeAll(list);
            list = hoaDonService.getAll();
            showResult(list);
        }
    }


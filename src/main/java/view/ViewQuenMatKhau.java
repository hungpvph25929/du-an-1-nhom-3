    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        if (quenMKService.validateMaXacThuc(txtMaXacThuc.getText().trim()) == null) {
            if (Integer.parseInt(txtMaXacThuc.getText().trim()) == maXacThuc) {
                this.dispose();
                JOptionPane.showMessageDialog(this, "Xác nhận thành công, vui lòng đổi mật khẩu mới.");
                ViewDoiMatKhauMoi viewDoiMKMoi = new ViewDoiMatKhauMoi(txtTaiKhoan.getText().trim());
                viewDoiMKMoi.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Mã xác thực sai, vui lòng nhập lại.");
            }
        } else {
            JOptionPane.showMessageDialog(this, quenMKService.validateMaXacThuc(txtMaXacThuc.getText().trim()));
        }
    }//GEN-LAST:event_btnXacNhanActionPerformed


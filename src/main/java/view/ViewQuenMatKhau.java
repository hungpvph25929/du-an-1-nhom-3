    private void btnGuiMaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuiMaActionPerformed
        if (quenMKService.validateTaiKhoan(txtTaiKhoan.getText().trim()) == null) {
            final String username = "laptopgroup3@gmail.com";
            final String password = "lveekscgavporrkq";
            Properties prop = new Properties();
            prop.put("mail.smtp.host", "smtp.gmail.com");
            prop.put("mail.smtp.port", "587");
            prop.put("mail.smtp.auth", "true");
            prop.put("mail.smtp.starttls.enable", "true"); //TLS
            Session session = Session.getInstance(prop,
                    new Authenticator() {
                protected PasswordAuthentication getPasswordAuthentication() {
                    return new PasswordAuthentication(username, password);
                }
            });
            try {
                Message message = new MimeMessage(session);
                if (quenMKService.getOne(txtTaiKhoan.getText().trim()).equals("Tài khoản chính xác")) {
                    turnOn();
                    btnGuiMa.setText("Gửi lại mã");
                    JOptionPane.showMessageDialog(this, quenMKService.getOne(txtTaiKhoan.getText().trim()));
                    JOptionPane.showMessageDialog(this, "Đã gửi mã xác thực tới email, "
                            + "nhập mã xác thực để hoàn tất.");
                    message.setFrom(new InternetAddress("laptopgroup3@gmail.com"));
                    message.setRecipients(
                            Message.RecipientType.TO,
                            InternetAddress.parse(txtTaiKhoan.getText().trim())
                    );
                    message.setSubject("Forrgot password");
                    //Lấy ra tên trước @
                    String[] chuoiTen = txtTaiKhoan.getText().trim().split("@");
                    String tenTrongMail = "";
                    for (int i = 0; i < 1; i++) {
                        tenTrongMail = chuoiTen[i];
                    }
                    //random mật khẩu int
                    Random random = new Random();
                    maXacThuc = random.nextInt((9999 - 1111) + 1) + 1111;
                    //System.out.println(maXacThuc);
                    message.setText("Dear " + tenTrongMail + ", you have forgotten your password. "
                            + "Your verification code is " + maXacThuc + ".");
                    Transport.send(message);
                } else {
                    JOptionPane.showMessageDialog(this, quenMKService.getOne(txtTaiKhoan.getText().trim()));
                }
            } catch (MessagingException e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, quenMKService.validateTaiKhoan(txtTaiKhoan.getText().trim()));
        }
    }//GEN-LAST:event_btnGuiMaActionPerformed

